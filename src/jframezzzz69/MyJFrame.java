    public String capitalize(String s) {
        Scanner sc = new Scanner(s);
        String temp;
        s = "";
        while (sc.hasNext()) {
            temp = sc.next();
            s += " " + temp.substring(0, 1).toUpperCase() + temp.substring(1);
        }
        return s.trim();
    }

